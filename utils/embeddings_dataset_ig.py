"""
EmbeddingsDatasetIG: Extension of EmbeddingsDataset that also loads token strings.

Each .pt file generated by main_preprocess_ig.py contains:
    - label: int sentiment label
    - embeddings: [N, d] tensor
    - target_pos: (start, end) tuple
    - hops: optional tensor
    - tokens: list of token strings  <-- NEW

This dataset returns a 4-tuple instead of 3-tuple:
    ((left, target, right), label, hops, tokens)
"""

import glob
from typing import Optional

import torch
from torch.utils.data import Dataset, DataLoader
from sklearn.model_selection import train_test_split


class EmbeddingsDatasetIG(Dataset):
    def __init__(self, year: int, phase: str = 'Train', device=torch.device('cpu'), empty_ok=False):
        self.dir = f'data/embeddings_ig/{year}-{phase}'
        self.device = device
        self.length = len(glob.glob(f'{self.dir}/*.pt'))
        self.cache: dict = {}

        if not empty_ok and self.length == 0:
            raise ValueError(f"Could not find embeddings at {self.dir}. "
                             f"Run main_preprocess_ig.py first.")

    def __getitem__(self, item: int):
        if item in self.cache:
            return self.cache[item]

        data: dict = torch.load(f"{self.dir}/{item}.pt", map_location=self.device)

        label: torch.Tensor = torch.tensor(data['label'], requires_grad=False, device=self.device)
        embeddings: torch.Tensor = data['embeddings']
        target_pos: tuple = data['target_pos']
        hops: Optional[torch.Tensor] = data['hops']
        tokens: list[str] = data['tokens']

        target_index_start, target_index_end = target_pos
        left: torch.Tensor = embeddings[0:target_index_start]
        target: torch.Tensor = embeddings[target_index_start:target_index_end]
        right: torch.Tensor = embeddings[target_index_end:]

        result = (
            (left.to(self.device), target.to(self.device), right.to(self.device)),
            label,
            hops,
            tokens,  # NEW
        )

        self.cache[item] = result
        return result

    def __len__(self):
        return self.length

    def __repr__(self):
        return f"EmbeddingsDatasetIG({self.dir})"


def train_validation_split_ig(dataset: EmbeddingsDatasetIG, validation_size=0.2, seed=42):
    """Stratified train/validation split for EmbeddingsDatasetIG."""
    loader = DataLoader(dataset, collate_fn=lambda batch: batch)
    labels: list[int] = [data[0][1].item() for data in loader]

    train_idx, validation_idx = train_test_split(
        range(len(dataset)),
        test_size=validation_size,
        shuffle=True,
        stratify=labels,
        random_state=seed
    )

    return train_idx, validation_idx
